# bootstrap
def bootstrap_rmse_ci(y_true, y_pred, n_bootstrap=200, ci=0.95):
    boot_rmse = []
    n = len(y_true)

    for _ in range(n_bootstrap):
        idx = resample(range(n), replace=True, n_samples=n)
        rmse = np.sqrt(np.mean((y_true[idx] - y_pred[idx]) ** 2))
        boot_rmse.append(rmse)

    lower = np.percentile(boot_rmse, (1-ci)/2 * 100)
    upper = np.percentile(boot_rmse, (1+ci)/2 * 100)
    return (lower, upper)

