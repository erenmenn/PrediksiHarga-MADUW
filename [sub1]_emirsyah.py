from sklearn.compose import ColumnTransformer
from sklearn.pipeline import Pipeline
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, mean_absolute_percentage_error


# 1. Pisahkan fitur dan target khusus Linear Regression
X_linreg = df_new.drop(columns=['Price', 'log_price'])
y_linreg = df_new['Price']

# 2. Identifikasi fitur numerik & kategori
cat_features_lr = ['Pollen_analysis']
num_features_lr = [c for c in X_linreg.columns if c not in cat_features_lr]

# 3. Preprocessor untuk Linear Regression
preprocessor_lr = ColumnTransformer([
    ('categorical', OneHotEncoder(drop='first'), cat_features_lr),
    ('numerical', 'passthrough', num_features_lr)
])

# 4. Pipeline Linear Regression
model_lr = Pipeline([
    ('preprocess', preprocessor_lr),
    ('lr', LinearRegression())
])

# 5. Split data
X_train_lr, X_test_lr, y_train_lr, y_test_lr = train_test_split(
    X_linreg, y_linreg, test_size=0.2, random_state=42
)

# 6. Train
model_lr.fit(X_train_lr, y_train_lr)

# 7. Predict
y_pred_lr = model_lr.predict(X_test_lr)

# 8. Evaluasi
rmse_lr = np.sqrt(mean_squared_error(y_test_lr, y_pred_lr))
mape_lr = mean_absolute_percentage_error(y_test_lr, y_pred_lr) * 100

print("RMSE (Linear Regression):", rmse_lr)
print("MAPE (%) (Linear Regression):", mape_lr)

